package org.example.e2.sop;

public class SystemOutPrintExercise {
    public static void main(String[] args) {
        System.out.println(10 + 10 + " Java" );
        System.out.println("Java " + 10 + 10);
        System.out.println("Java " + 10 * 10);
        System.out.println("Java " + (10 + 10));

        /*
        -----output-----
        20 Java
        Java 1010
        Java 100
        Java 20
         */
    }
}
